"use client";

import { useState } from "react";
import { useRouter } from "next/navigation";

interface ContactOptionsProps {
  sellerName: string;
  sellerPhone: string;
  ticketId: string;
  eventName: string;
  transactionId: string;
}

export default function ContactOptions({
  sellerName,
  sellerPhone,
  ticketId,
  eventName,
  transactionId,
}: ContactOptionsProps) {
  const router = useRouter();
  const [loading, setLoading] = useState(false);

  const formatPhoneForWhatsApp = (phone: string) => {
    const numbers = phone.replace(/\D/g, "");
    // Adiciona 55 se não tiver código do país
    if (numbers.length <= 11) {
      return `55${numbers}`;
    }
    return numbers;
  };

  const handleWhatsApp = () => {
    const formattedPhone = formatPhoneForWhatsApp(sellerPhone);
    const message = encodeURIComponent(
      `Ola ${sellerName}! Comprei o ingresso "${eventName}" pelo Passback. Podemos combinar a entrega?`
    );
    window.open(`https://wa.me/${formattedPhone}?text=${message}`, "_blank");
  };

  const handleSiteChat = async () => {
    setLoading(true);
    try {
      // Inicia conversa no chat do site
      const response = await fetch("/api/modules/chat/start", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          ticketId,
          transactionId,
        }),
      });

      const data = await response.json();

      if (response.ok && data.conversation) {
        router.push(`/mensagens?chat=${data.conversation.id}`);
      } else {
        // Se falhar, vai para a página de mensagens
        router.push("/mensagens");
      }
    } catch {
      router.push("/mensagens");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="bg-[#16C784]/10 border border-[#16C784]/20 p-5 rounded-xl">
      <h4 className="font-semibold text-[#16C784] mb-3 flex items-center gap-2">
        <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
        </svg>
        Pagamento confirmado!
      </h4>

      <p className="text-sm text-gray-300 mb-4">
        Agora voce precisa entrar em contato com <strong className="text-white">{sellerName}</strong> para combinar a entrega do ingresso. Escolha como prefere conversar:
      </p>

      <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
        {/* WhatsApp Button */}
        <button
          onClick={handleWhatsApp}
          className="flex items-center justify-center gap-3 py-4 px-4 rounded-xl bg-[#25D366] hover:bg-[#20BD5A] text-white font-semibold transition-all"
        >
          <svg className="w-6 h-6" fill="currentColor" viewBox="0 0 24 24">
            <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z"/>
          </svg>
          Conversar pelo WhatsApp
        </button>

        {/* Site Chat Button */}
        <button
          onClick={handleSiteChat}
          disabled={loading}
          className="flex items-center justify-center gap-3 py-4 px-4 rounded-xl bg-[#1A3A5C] hover:bg-[#234B6E] text-white font-semibold border border-white/10 transition-all disabled:opacity-50"
        >
          {loading ? (
            <svg className="w-5 h-5 animate-spin" fill="none" viewBox="0 0 24 24">
              <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4" />
              <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4z" />
            </svg>
          ) : (
            <svg className="w-6 h-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 12h.01M12 12h.01M16 12h.01M21 12c0 4.418-4.03 8-9 8a9.863 9.863 0 01-4.255-.949L3 20l1.395-3.72C3.512 15.042 3 13.574 3 12c0-4.418 4.03-8 9-8s9 3.582 9 8z" />
            </svg>
          )}
          Chat do Passback
        </button>
      </div>

      <p className="text-xs text-gray-500 mt-3 text-center">
        Voce pode conversar pelos dois canais - suas mensagens ficam salvas no chat do site
      </p>
    </div>
  );
}
